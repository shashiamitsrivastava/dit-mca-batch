public strictfp class JavaDataTypes {
    public strictfp static void main(String[] args) {
        // Primitive Data Types
        // Value Type
        // Immutable Type
        byte age = 22;
        int days = 11111;
        long w = 99999999L;
        float e1 = 90.20F;
        double e2 = 90.344;
        boolean e4 = true;
        char t2 = 'рди';
        System.out.println(t2);
        int a = 10;
        int b = a;
        //b = b + 10;
        System.out.println(b+10); // 20
        System.out.println(b); // 10

    }
}
